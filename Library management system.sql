use library;
--  book table
CREATE TABLE book (
    book_id INT NOT NULL,
    book_name VARCHAR(100),
    book_category VARCHAR(100),
    author_name VARCHAR(100),
    Date_Of_Arrival DATE,
    book_status ENUM('Available', 'Not Available'),
    CONSTRAINT pk_book_id PRIMARY KEY (book_id)
);

-- publisher table
CREATE TABLE publisher (
    pub_id INT NOT NULL,
    pub_name VARCHAR(100),
    pub_address VARCHAR(100),
    book_id INT,
    CONSTRAINT pk_pub_id PRIMARY KEY (pub_id),
    CONSTRAINT fk_book_id FOREIGN KEY (book_id) REFERENCES books(book_id)
);
-- borrower
CREATE TABLE borrower (
    borrow_cardno INT NOT NULL,
    borrow_join_date DATE,
    borrow_name VARCHAR(25),
    borrow_age INT,
    borrow_mobileno BIGINT,
    borrow_emailid VARCHAR(100),
    borrow_address VARCHAR(100),
    borrow_fees DOUBLE,
    CONSTRAINT pk_borrow_cardno PRIMARY KEY (borrow_cardno)
);
-- branch_info
CREATE TABLE branch_info (
    branch_id INT NOT NULL,
    branch_name VARCHAR(200),
    branch_address VARCHAR(100),
    startTime TIME,
    endTime TIME,
    book_id INT,
    CONSTRAINT pk_branch_id PRIMARY KEY (branch_id),
    CONSTRAINT fk_book_id FOREIGN KEY (book_id) REFERENCES books(book_id)
);
-- lending_book
CREATE TABLE lending_book (
    lending_cardno INT,
    lending_book_id INT,
    lending_branch_id INT,
    comment SET('check out', 'return', 'renewal'),
    checkoutDate DATE,
    dueDate DATE,
    renewalDate DATE,
    returnedDate DATE,
    CONSTRAINT fk_lending_cardno FOREIGN KEY (lending_cardno) REFERENCES borrowers(borrow_cardno),
    CONSTRAINT fk_lending_book_id FOREIGN KEY (lending_book_id) REFERENCES books(book_id),
    CONSTRAINT fk_lending_branch_id FOREIGN KEY (lending_branch_id) REFERENCES branches(branch_id)
);

